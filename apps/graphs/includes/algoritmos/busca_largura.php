    <i>"O percurso em largura (ou busca em largura) visita 
        todos os sucessores de um nó visitado antes de visitar
        quaisquer sucessores de qualquer um desses sucessores (...).
        Enquanto o percurso em profundidade tende a criar árvores estreitas, 
        muito longas, o percurso em largura tende a criar árvores 
        baixas e muito largas. "</i> <b>(Tenenbaum)</b>
        <br/>
    Ou seja, a busca em largura visita todos os nós da mesma 
    altura na arvore até partir para o próximo nível.
