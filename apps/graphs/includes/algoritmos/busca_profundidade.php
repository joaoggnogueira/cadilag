    <i>"Uma busca em profundidade, também chamado de Percurso em profundidade, 
        como o próprio nome indica, 
        percorre um único caminho do grafo até onde ele possa chegar 
        (isto é, até visitar um nó sem sucessores ou um nó cujos 
        sucessores já tenham sido visitados). 
        Em seguida, ele continua no último nó no 
        caminho recém-percorrido que tenha um sucessor 
        não-visitado e começa a percorrer um novo caminho emanando 
        a partir desse nó. As árvores geradoras criadas por um percurso 
        em profundidade por nível tendem a ser muito profundas."</i> <b>(Tenenbaum)</b>
